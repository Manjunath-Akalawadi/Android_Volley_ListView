package com.manju7.volley_list;

public class Items {

    String name;
    String desc;
    String age;

    public Items(String name, String desc, String age) {
        this.name = name;
        this.desc = desc;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getDesc() {
        return desc;
    }

    public String getAge() {
        return age;
    }
}
